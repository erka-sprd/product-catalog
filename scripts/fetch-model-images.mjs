#!/usr/bin/env node
// Fetch a per-colour (per-appearance) front model image for every embroidery
// product, so the designer can show the model in the same colour the user picks
// on the canvas. Surgically updates lib/products-data.ts in place — every other
// field is preserved. Adds `modelImage: string | null` to each appearance and
// repoints each product's `modelImageFront` to its default appearance's shot.
//
// Requires create-omat running (model metadata has no public endpoint).
// Run: `node scripts/fetch-model-images.mjs`

import { mkdir, readFile, stat, writeFile } from "node:fs/promises"
import { dirname, join } from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, "..")
const IMG_ROOT = join(ROOT, "public", "products")
const DATA_FILE = join(ROOT, "lib", "products-data.ts")

const IMG_WIDTH = 600
// Per-product preferred model-shot tag. Some products (e.g. the polo) default to
// a two-person "unisex" shot at the highest priority; force a single-person shot
// by preferring its gender tag instead.
const MODEL_TAG_OVERRIDE = {
  "2116": "male", // polo — single male model rather than the two-person shot
}
const MODEL_META_BASE = process.env.MODEL_META_BASE || "http://localhost:3000"
const MODEL_META_SHOP = process.env.MODEL_META_SHOP || "1133169"
const MODEL_IMAGE_SERVER = "https://image.spreadshirtmedia.net/image-server/v1"

async function fetchJson(url) {
  const res = await fetch(url)
  if (!res.ok) throw new Error(`${res.status} ${url}`)
  return res.json()
}

async function fetchOmatAssortment(productTypeId) {
  try {
    const data = await fetchJson(
      `${MODEL_META_BASE}/api/assortment/${productTypeId}?mediaType=json&shopId=${MODEL_META_SHOP}&locale=en_GB`
    )
    return data && !data.error ? data : null
  } catch {
    return null
  }
}

async function downloadImage(url, path) {
  try {
    await stat(path)
    return true // already downloaded
  } catch {}
  await mkdir(dirname(path), { recursive: true })
  const res = await fetch(url)
  if (!res.ok) throw new Error(`${res.status} ${url}`)
  const buf = Buffer.from(await res.arrayBuffer())
  await writeFile(path, buf)
  return true
}

// Build the front-view (viewId 1) model-image URL for one specific appearance,
// preferring an on-model (non-flatlay) shot at the highest priority. null if
// no model shot covers this appearance.
function modelImageUrlForAppearance(modelImages, productTypeId, appearanceId) {
  const front = (modelImages ?? []).filter(m => m.viewId === 1 && m.active !== false)
  const matching = front.filter(m =>
    (m.appearanceIds ?? []).map(String).includes(String(appearanceId))
  )
  if (matching.length === 0) return null
  const sorted = matching.sort((a, b) => (b.priority ?? 0) - (a.priority ?? 0))
  const onModel = sorted.filter(m => !(m.tags ?? []).includes("flatlay"))
  const preferTag = MODEL_TAG_OVERRIDE[productTypeId]
  const chosen =
    (preferTag && onModel.find(m => (m.tags ?? []).includes(preferTag))) ??
    onModel[0] ??
    sorted[0]
  const crop = (chosen.crops ?? []).includes("detail") ? "detail" : (chosen.crops?.[0] ?? "detail")
  return `${MODEL_IMAGE_SERVER}/productTypes/${productTypeId}/views/1,modelId=${chosen.modelId},crop=${crop},appearanceId=${appearanceId},backgroundColor=F4F4F4`
}

// Import the current PRODUCTS array out of the TS data file.
async function loadProducts() {
  let src = await readFile(DATA_FILE, "utf8")
  src = src
    .replace(/export type[\s\S]*?\n}\n/g, "")
    .replace("export const PRODUCTS: StaticProduct[] =", "export const PRODUCTS =")
  const mod = await import("data:text/javascript," + encodeURIComponent(src))
  return { PRODUCTS: mod.PRODUCTS, FEATURED_PRODUCT_ID: mod.FEATURED_PRODUCT_ID }
}

function serialize(products, featuredId) {
  return `// Auto-generated by scripts/fetch-products.mjs. Do not edit by hand.

export type StaticViewMap = {
  printAreaId: string
  offset: { x: number; y: number; unit: string }
  size: { width: number; height: number; unit: string }
  dpi: number
}

export type StaticView = {
  id: string
  name: string
  canvas: { width: number; height: number; unit: string }
  dpi: number
  viewMaps: StaticViewMap[]
}

export type StaticAppearanceView = {
  id: string
  image: string
}

export type StaticPrintType = {
  id: string
  href: string
}

export type StaticAppearance = {
  id: string
  name: string
  color: string
  image: string
  modelImage: string | null
  printTypes: StaticPrintType[]
  views: StaticAppearanceView[]
}

export type StaticPrintArea = {
  id: string
  defaultViewId: string
  boundary: { width: number; height: number; unit: string }
  restrictions: { textAllowed: boolean; designAllowed: boolean; backgroundAllowed: boolean }
  printoutQuantity: number
}

export type StaticProductDetails = {
  shortDescription: string
  description: string
  brand: string
  weight: number
  sizeFitHint: string
}

export type StaticProduct = {
  id: string
  name: string
  price: number
  preview: string
  embroidery: boolean
  modelImageFront: string | null
  appearances: StaticAppearance[]
  views: StaticView[]
  sizes: string[]
  printAreas: StaticPrintArea[]
  details: StaticProductDetails
}

export const FEATURED_PRODUCT_ID = ${JSON.stringify(featuredId)}

export const PRODUCTS: StaticProduct[] = ${JSON.stringify(products, null, 2)}
`
}

async function main() {
  const { PRODUCTS, FEATURED_PRODUCT_ID } = await loadProducts()
  let totalImgs = 0

  for (const p of PRODUCTS) {
    // Ensure the field exists on every appearance (default null) for type safety.
    for (const a of p.appearances) if (a.modelImage === undefined) a.modelImage = null

    if (!p.embroidery) {
      p.modelImageFront = null
      continue
    }

    const omat = await fetchOmatAssortment(p.id)
    if (!omat) {
      console.warn(`  ${p.id} ${p.name}: no create-omat assortment — skipped`)
      continue
    }

    let got = 0
    for (const a of p.appearances) {
      const url = modelImageUrlForAppearance(omat.modelImages, p.id, a.id)
      if (!url) {
        a.modelImage = null
        continue
      }
      const rel = `/products/${p.id}/${a.id}/model.webp`
      const file = join(IMG_ROOT, p.id, a.id, "model.webp")
      try {
        await downloadImage(`${url}?width=${IMG_WIDTH}`, file)
        a.modelImage = rel
        got++
        totalImgs++
      } catch (e) {
        console.warn(`    skip model ${p.id}/${a.id}: ${e.message}`)
        a.modelImage = null
      }
    }

    // Default-appearance model shot drives modelImageFront (fallback to any).
    const def =
      p.appearances.find(a => a.image === p.preview) ??
      p.appearances.find(a => a.modelImage) ??
      p.appearances[0]
    p.modelImageFront = def?.modelImage ?? p.appearances.find(a => a.modelImage)?.modelImage ?? null

    const missing = p.appearances.filter(a => !a.modelImage).map(a => a.id)
    console.log(
      `  ${p.id} ${p.name}: ${got}/${p.appearances.length} colours` +
        (missing.length ? `  (no shot: ${missing.join(",")})` : "")
    )
  }

  await writeFile(DATA_FILE, serialize(PRODUCTS, FEATURED_PRODUCT_ID))
  console.log(`\nWrote ${DATA_FILE} — ${totalImgs} model images downloaded`)
}

main().catch(err => {
  console.error(err)
  process.exit(1)
})
